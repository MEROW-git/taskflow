type TranslationKey = string;

const interpolate = (template: string, params?: Record<string, string | number>) => {
  if (!params) return template;

  return Object.entries(params).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template
  );
};

const resolveTranslation = (language: AppLanguage, key: TranslationKey): TranslationValue | undefined => {
  return key.split('.').reduce<any>((acc, part) => {
    if (!acc || typeof acc === 'string') return undefined;
    return acc[part];
  }, translations[language]) as TranslationValue | undefined;
};

export const translate = (
  language: AppLanguage,
  key: TranslationKey,
  params?: Record<string, string | number>
) => {
  const value = resolveTranslation(language, key);
  if (typeof value !== 'string') return key;
  return interpolate(value, params);
};

export const useI18n = () => {
  const { language } = useSettingsStore();

  return {
    language,
    t: (key: TranslationKey, params?: Record<string, string | number>) =>
      translate(language, key, params),
    labelTaskStatus: (status: TaskStatus) =>
      translate(
        language,
        status === 'in-progress' ? 'labels.inProgress' : `labels.${status}`
      ),
    labelPriority: (priority: TaskPriority) => translate(language, `labels.${priority}`),
    labelTaskType: (taskType: TaskType) => translate(language, `labels.${taskType}`),
    labelCategory: (category: string) =>
      translate(language, `labels.${category}`),
  };
};
